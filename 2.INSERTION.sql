--Sin embargo, es posible lograr un resultado similar en SQLite utilizando un enfoque diferente, ya que SQLite no soporta procedimientos almacenados o bucles de manera nativa. Tendrías que utilizar scripts externos, como scripts en Python, para ejecutar múltiples inserciones o aprovechar las características de las consultas de SQL que sí están soportadas por SQLite.
--Alternativa para SQLite usando Python
--Para replicar la lógica de asignar cursos aleatoriamente a estudiantes en SQLite, puedes utilizar un script en Python. Aquí hay un ejemplo básico:
import sqlite3
import random
from datetime import datetime, timedelta

# Conectar a la base de datos SQLite
conn = sqlite3.connect('school.db')
cursor = conn.cursor()

# Función para generar una fecha aleatoria en 2023
def random_date():
    start_date = datetime(2023, 1, 1)
    end_date = datetime(2023, 12, 31)
    delta = end_date - start_date
    random_days = random.randint(0, delta.days)
    return start_date + timedelta(days=random_days)

# Insertar datos aleatorios
for student_id in range(6, 51):  # Para estudiantes con IDs del 6 al 50
    courses_count = random.randint(1, 8)  # Número aleatorio de cursos entre 1 y 8
    course_ids = cursor.execute('SELECT COURSESID FROM COURSES ORDER BY RANDOM() LIMIT ?', (courses_count,)).fetchall()
    
    for course_id in course_ids:
        enroll_date = random_date().date()
        cursor.execute('INSERT INTO STUDENT_COURSE (STUDENTID, COURSESID, ENROLLMENT_DATE) VALUES (?, ?, ?)',
                       (student_id, course_id[0], enroll_date))

# Guardar cambios y cerrar la conexión
conn.commit()
conn.close()
